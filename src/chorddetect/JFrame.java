    private void jComboBoxChordActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jComboBoxChordActionPerformed
        // TODO add your handling code here:t
        String position = (String) jComboBoxChord.getSelectedItem();
        String chordName = c.getChord(position);
        String[] notesForChord = c.getNotesForChord(Useful.restCharacter(chordName));
        notesForChord = notesForChord[1].split("-");
        chordName += " notas: ";
        for (String string : notesForChord) {
            chordName += string + " ";
        }
        jLabelNotesChord.setText(chordName);
    }//GEN-LAST:event_jComboBoxChordActionPerformed

