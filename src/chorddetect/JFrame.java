        jFrameChord.setVisible(true);
        jFrameChord.setBounds(0, 0, 500, 255);
        ComboBoxModel<String> cbm = new DefaultComboBoxModel<>(c.getNameChords());
        jComboBoxChord.setModel(cbm);
        ComboBoxModel<String> cbm2 = new DefaultComboBoxModel<>(c.getChords());
        jComboBoxChordName.setModel(cbm2);

        //modificar los array para que tenga un espacio vacio
        String[] notasTmp = c.getNotes();
        String[] notasFinal = new String[notasTmp.length + 1];
        notasFinal[0] = "";
        for (int i = 1; i < notasFinal.length; i++) {
            notasFinal[i] = notasTmp[i - 1];
        }

        //notas para los selectores
        ComboBoxModel<String> cbmNotes1 = new DefaultComboBoxModel<>(notasTmp);
        ComboBoxModel<String> cbmNotes2 = new DefaultComboBoxModel<>(notasTmp);
        ComboBoxModel<String> cbmNotes3 = new DefaultComboBoxModel<>(notasTmp);
        ComboBoxModel<String> cbmNotes4 = new DefaultComboBoxModel<>(notasFinal);

        jComboBoxNote1.setModel(cbmNotes1);
        jComboBoxNote2.setModel(cbmNotes2);
        jComboBoxNote3.setModel(cbmNotes3);
        jComboBoxNote4.setModel(cbmNotes4);
