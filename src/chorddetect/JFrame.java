    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String note1 = (String) jComboBoxNote1.getSelectedItem();
        String note2 = (String) jComboBoxNote2.getSelectedItem();
        String note3 = (String) jComboBoxNote3.getSelectedItem();
        String note4 = (String) jComboBoxNote4.getSelectedItem();

        String[] notes;
        if (note4.isEmpty()) {
            notes = new String[3];
            notes[0] = note1;
            notes[1] = note2;
            notes[2] = note3;
        } else {
            notes = new String[4];
            notes[0] = note1;
            notes[1] = note2;
            notes[2] = note3;
            notes[3] = note4;
        }
        String[] chord = c.getChordForNotes(notes);
        jLabelchordName.setText(chord[1]);
        if (chord[1].isEmpty()) {
            jLabelchordName.setText("Notas no validas");
        } else {
            jLabelchordName.setText(Useful.restCharacter(chord[1]) + ", cifrado: " + Useful.restCharacter(chord[0]));
        }
    }//GEN-LAST:event_jButton1ActionPerformed

